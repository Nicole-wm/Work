$(document).ready(function () {
    $("#PopTitleClose").click(function () {
        $("#mask").remove();
        $("#PopPanel").remove();
    });

    $("#ConfirmBtn").click(function () {
        $("#mask").remove();
        $("#PopPanel").remove();
    });

    $("#CancelBtn").click(function () {
        $("#mask").remove();
        $("#PopPanel").remove();
    });
});